package com.achilles.players.dto;

public class SeasonRound {
	private String name;
	private int roundPhase;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getRoundPhase() {
		return roundPhase;
	}
	public void setRoundPhase(int roundPhase) {
		this.roundPhase = roundPhase;
	}
}
